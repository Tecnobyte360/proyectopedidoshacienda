    <h1>Eliminación de datos</h1>
    <p class="muted">Aplicación <strong>Kivox Repartidores</strong> (paquete <code>co.kivox.repartidores</code>) · Responsable: <strong>Tecnobyte360</strong></p>

    <p>Si usas la aplicación <strong>Kivox Repartidores</strong> de <strong>Tecnobyte360</strong>, tienes derecho a
    solicitar la eliminación de tus datos personales almacenados en la plataforma.</p>

    <h2>Cómo solicitar la eliminación</h2>
    <p>Envía un correo a <a href="mailto:comercial@tecnobyte360.com">comercial@tecnobyte360.com</a> desde la cuenta
    asociada a tu usuario, con el asunto <strong>"Eliminación de datos - Kivox Repartidores"</strong>, indicando tu
    nombre y el número de teléfono o correo con el que ingresas a la app.</p>
    <p>Verificaremos tu identidad y procesaremos el borrado en un plazo máximo de <strong>30 días</strong>.</p>

    <h2>Qué datos se eliminan</h2>
    <ul>
        <li>Nombre, correo electrónico y número de teléfono de tu cuenta.</li>
        <li>Datos de ubicación (GPS) registrados durante tus entregas.</li>
        <li>Identificadores del dispositivo (token de notificaciones).</li>
        <li>Fotos de comprobante y datos de las entregas asociados a tu usuario.</li>
    </ul>

    <h2>Qué datos podríamos conservar</h2>
    <p>Por obligaciones legales o contables, podríamos conservar de forma anonimizada ciertos registros de las
    operaciones (por ejemplo, totales de entregas) que ya no permiten identificarte personalmente. Estos se conservan
    únicamente por el período exigido por la ley y luego se eliminan.</p>

