    <h1>Política de Privacidad</h1>
    <p class="muted">Aplicación <strong>Kivox Repartidores</strong> (paquete <code>co.kivox.repartidores</code>)<br>
    Responsable: <strong>Tecnobyte360</strong> · Última actualización: 29 de junio de 2026</p>

    <p>Esta Política describe cómo la aplicación <strong>Kivox Repartidores</strong> ("la App"), operada por
    <strong>Tecnobyte360</strong>, recopila, usa y protege la información de los repartidores (domiciliarios) que la utilizan
    para gestionar entregas a domicilio por cuenta de los comercios afiliados a la plataforma Kivox.</p>

    <h2>1. Información que recopilamos</h2>
    <ul>
        <li><strong>Datos de cuenta:</strong> nombre, correo electrónico y teléfono del repartidor, usados para iniciar sesión.</li>
        <li><strong>Ubicación precisa (GPS), incluso en segundo plano:</strong> mientras el repartidor está en servicio,
            la App obtiene su ubicación —aunque la aplicación esté minimizada o la pantalla apagada— para mostrar al
            comercio el avance de la entrega y asignar pedidos cercanos.</li>
        <li><strong>Identificadores del dispositivo:</strong> token de notificaciones (Firebase Cloud Messaging) para
            enviar avisos de nuevos pedidos y cambios de estado.</li>
        <li><strong>Información de las entregas:</strong> pedidos asignados, estados (en camino, entregado, no entregado),
            código de entrega, motivo de no entrega y fotos de comprobante que el repartidor decida tomar.</li>
    </ul>

    <h2>2. Para qué usamos la información</h2>
    <ul>
        <li>Asignar y gestionar los domicilios del repartidor.</li>
        <li>Compartir en tiempo real la ubicación del repartidor con el <strong>comercio</strong> al que presta servicio,
            únicamente durante entregas activas, para coordinar y dar seguimiento al pedido.</li>
        <li>Enviar notificaciones push de nuevos pedidos y actualizaciones.</li>
        <li>Validar la entrega (código y/o foto de comprobante).</li>
    </ul>

    <h2>3. Uso de ubicación en segundo plano</h2>
    <p>La App utiliza la ubicación en <strong>segundo plano</strong> exclusivamente para el seguimiento de la ruta de
    entrega mientras el repartidor se encuentra <strong>en servicio</strong>. El repartidor concede este permiso de forma
    explícita y puede revocarlo en cualquier momento desde los ajustes del dispositivo o cerrando su sesión en la App.
    No se rastrea la ubicación cuando el repartidor no está en servicio.</p>

    <h2>4. Con quién compartimos la información</h2>
    <p>No vendemos ni alquilamos datos personales. La información se comparte únicamente con:</p>
    <ul>
        <li>El <strong>comercio</strong> al que el repartidor presta servicio, para la gestión de las entregas.</li>
        <li>Proveedores tecnológicos necesarios para operar la App (por ejemplo, <strong>Google Firebase</strong> para
            notificaciones), bajo sus respectivas políticas.</li>
        <li>Autoridades, cuando la ley lo exija.</li>
    </ul>

    <h2>5. Conservación y seguridad</h2>
    <p>Conservamos los datos el tiempo necesario para prestar el servicio y cumplir obligaciones legales. Aplicamos
    medidas de seguridad razonables (cifrado en tránsito, control de acceso) para proteger la información.</p>

    <h2>6. Derechos del usuario</h2>
    <p>El repartidor puede solicitar el acceso, la corrección o la eliminación de sus datos escribiendo al correo de
    contacto. También puede desinstalar la App y revocar los permisos en cualquier momento.</p>

    <h2>7. Menores de edad</h2>
    <p>La App está dirigida a repartidores adultos y no está destinada a menores de edad.</p>

    <h2>8. Cambios a esta política</h2>
    <p>Podemos actualizar esta Política; publicaremos la versión vigente en esta misma dirección.</p>

    <h2>9. Contacto</h2>
